package src.Dec20;

public class Lab005 {

    public static void main(String[] args)
    {
        // Why we are using this?
        // Formatting purpose
        String name = "Sonam"; // Bunch of Char
        // Concate +
        System.out.println("Your name is -> " + name);

        // Formatting ( printf)
        System.out.printf("Your name is -> %s",name);

        System.out.println();

        float pi = 3.14159f;
        System.out.printf("Your value is  %f",pi);
    }
}
